    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        //Codigo que permite consultar registros en la base de datos
        try {
            Connection cn = DriverManager.getConnection(Multibodega.Base_de_Datos, Multibodega.Usuario, Multibodega.Clave);
            PreparedStatement pst = cn.prepareStatement("select * from Venta_Detalle where Orden_Venta = ?");
            pst.setString(1, txt_buscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txt_Orden_Venta.setText(rs.getString("Orden_Venta"));
                txt_Precio_Venta.setText(rs.getString("Precio_Venta"));
                txt_Cantidad_Venta.setText(rs.getString("Cantidad"));
                txt_Costo_Venta.setText(rs.getString("Costo_Venta"));
                txt_ID_Inventario.setText(rs.getString("Id_Inventario"));
                txt_ID_Productos.setText(rs.getString("Id_Productos"));
                txt_ID_VentaE.setText(rs.getString("Id_Venta_Encabezado"));

            } else {
                JOptionPane.showMessageDialog(null, "Venta Detalle no registrada.");
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

