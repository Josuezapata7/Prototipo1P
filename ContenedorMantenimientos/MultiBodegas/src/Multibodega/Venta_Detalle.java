    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Codigo que permite modificar registros en la base de datos
        try {
            String ID = txt_buscar.getText().trim();

            Connection cn = DriverManager.getConnection(Multibodega.Base_de_Datos, Multibodega.Usuario, Multibodega.Clave);
            PreparedStatement pst = cn.prepareStatement("update Venta_Detalle set Orden_Venta = ?, Precio_Venta=?, Cantidad=?, Costo_Venta=?, Id_Inventario=?, Id_Productos=?, Id_Venta_Encabezado=? where Orden_Venta = " + ID);

            pst.setString(1, txt_Orden_Venta.getText().trim());
            pst.setString(2, txt_Precio_Venta.getText().trim());
            pst.setString(3, txt_Cantidad_Venta.getText().trim());
            pst.setString(4, txt_Costo_Venta.getText().trim());
            pst.setString(5, txt_ID_Inventario.getText().trim());
            pst.setString(6, txt_ID_Productos.getText().trim());
            pst.setString(7, txt_ID_VentaE.getText().trim());

            pst.executeUpdate();

            txt_buscar.setText("");
            lbl_estatus.setText("Modificaci√≥n exitosa.");

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

